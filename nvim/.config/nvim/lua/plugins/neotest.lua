local M = { "nvim-neotest/neotest" }

M.dependencies = {
  "nvim-neotest/nvim-nio",
  "nvim-lua/plenary.nvim",
  {
    "nvim-neotest/neotest-plenary",
    dependencies = { "nvim-neotest/neotest" },
  },
