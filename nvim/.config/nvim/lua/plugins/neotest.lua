    dependencies = { "nvim-neotest/neotest" },
  },
}

M.keys = {
  { "<leader>tr", function() require("neotest").run.run() end,                                    desc = "Neotest: Run nearest" },
  { "<leader>tf", function() require("neotest").run.run(vim.fn.expand("%")) end,                  desc = "Neotest: Run file" },
  { "<leader>ts", function() require("neotest").run.run({ suite = true }) end,                    desc = "Neotest: Run suite" },
  { "<leader>tl", function() require("neotest").run.run_last() end,                               desc = "Neotest: Run last" },
  { "<leader>to", function() require("neotest").output.open() end,                                desc = "Neotest: Toggle output" },
  { "<leader>tO", function() require("neotest").summary.toggle() end,                             desc = "Neotest: Toggle summary" },
  { "<leader>td", function() require("neotest").run.run({ strategy = "dap", suite = false }) end, desc = "Neotest: Debug nearest" },
}

